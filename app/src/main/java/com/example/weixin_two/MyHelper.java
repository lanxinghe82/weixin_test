package com.example.weixin_two;

public class MyHelper {
    private String name;
    private int img;
    private String text;
    public void setName(String name) {
        this.name = name;
    }
    public void setImg(int img) {
        this.img = img;
    }
    public void setText(String text) {
        this.text = text;
    }
    public String getName() {
        return name;
    }
    public String getText() {
        return text;
    }
    public int getImg() {
        return img;
    }

}
